class Solution
{
	public:
		string is_palindrome(int n)
		{
		    int temp = n,temp2=0;
		    while(n){
		        int dig = n%10;
		        temp2 =temp2*10+ dig;
		        n/=10;
		    }
		    
		   if(temp==temp2)return "Yes";
		   return  "No";
		}
};

